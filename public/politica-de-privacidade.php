                            <section class="mb-5">
                                <h2 class="h3 text-primary mb-3">1. Introdução</h2>
                                <p>A ClubeMix está comprometida em proteger sua privacidade e dados pessoais. Esta Política de Privacidade explica como coletamos, usamos, armazenamos e protegemos suas informações quando você utiliza nossa plataforma.</p>
                                <p>Esta política está em conformidade com a Lei Geral de Proteção de Dados (LGPD - Lei 13.709/2018) e outras regulamentações aplicáveis.</p>
                            </section>

                            <section class="mb-5">
                                <h2 class="h3 text-primary mb-3">2. Informações que Coletamos</h2>
                                
                                <h4 class="h5 text-secondary">2.1 Dados Pessoais Fornecidos por Você</h4>
                                <ul>
                                    <li><strong>Dados de Identificação:</strong> Nome completo, CPF/CNPJ, data de nascimento</li>
                                    <li><strong>Dados de Contato:</strong> E-mail, telefone, WhatsApp</li>
                                    <li><strong>Dados de Endereço:</strong> CEP, rua, número, complemento, bairro, cidade, estado</li>
                                    <li><strong>Dados Financeiros:</strong> Chave PIX para pagamentos</li>
                                    <li><strong>Dados de Empresa:</strong> Razão social, CNPJ, segmento de atuação (para empresas parceiras)</li>
                                </ul>

                                <h4 class="h5 text-secondary mt-4">2.2 Dados Coletados Automaticamente</h4>
                                <ul>
                                    <li>Informações de navegação (páginas visitadas, tempo de permanência)</li>
                                    <li>Dados técnicos (endereço IP, tipo de navegador, sistema operacional)</li>
                                    <li>Cookies e tecnologias similares</li>
                                    <li>Dados de geolocalização (quando autorizado)</li>
                                </ul>

                                <h4 class="h5 text-secondary mt-4">2.3 Dados de Terceiros</h4>
                                <ul>
                                    <li>Informações de empresas parceiras sobre transações</li>
                                    <li>Dados de verificação de identidade e antifraude</li>
                                    <li>Informações de redes sociais (quando você escolhe conectar)</li>
                                </ul>
                            </section>

                            <section class="mb-5">
                                <h2 class="h3 text-primary mb-3">3. Como Utilizamos suas Informações</h2>
                                <p>Utilizamos seus dados pessoais para as seguintes finalidades:</p>
                                
                                <h4 class="h5 text-secondary">3.1 Prestação de Serviços</h4>
                                <ul>
                                    <li>Criar e gerenciar sua conta na plataforma</li>
                                    <li>Processar transações e calcular bonificações</li>
                                    <li>Realizar pagamentos via PIX</li>
                                    <li>Fornecer suporte ao cliente</li>
                                </ul>

                                <h4 class="h5 text-secondary mt-4">3.2 Comunicação</h4>
                                <ul>
                                    <li>Enviar notificações sobre sua conta e transações</li>
                                    <li>Comunicar alterações nos nossos serviços</li>
                                    <li>Responder suas dúvidas e solicitações</li>
                                    <li>Enviar materiais promocionais (com seu consentimento)</li>
                                </ul>

                                <h4 class="h5 text-secondary mt-4">3.3 Segurança e Conformidade</h4>
                                <ul>
                                    <li>Prevenir fraudes e atividades suspeitas</li>
                                    <li>Cumprir obrigações legais e regulatórias</li>
                                    <li>Proteger nossos direitos e propriedade</li>
                                    <li>Resolver disputas</li>
                                </ul>

                                <h4 class="h5 text-secondary mt-4">3.4 Melhorias e Análises</h4>
                                <ul>
                                    <li>Analisar o uso da plataforma para melhorias</li>
                                    <li>Desenvolver novos recursos e serviços</li>
                                    <li>Realizar pesquisas de mercado</li>
                                    <li>Gerar estatísticas agregadas e anônimas</li>
                                </ul>
                            </section>

                            <section class="mb-5">
                                <h2 class="h3 text-primary mb-3">4. Base Legal para o Tratamento</h2>
                                <p>Tratamos seus dados pessoais com base nas seguintes hipóteses legais:</p>
                                <ul>
                                    <li><strong>Execução de Contrato:</strong> Para fornecer os serviços solicitados</li>
                                    <li><strong>Consentimento:</strong> Para comunicações promocionais e cookies não essenciais</li>
                                    <li><strong>Legítimo Interesse:</strong> Para segurança, prevenção de fraudes e melhorias</li>
                                    <li><strong>Obrigação Legal:</strong> Para cumprimento de leis e regulamentos</li>
                                </ul>
                            </section>

                            <section class="mb-5">
                                <h2 class="h3 text-primary mb-3">5. Compartilhamento de Informações</h2>
                                <p>Podemos compartilhar suas informações nas seguintes situações:</p>
                                
                                <h4 class="h5 text-secondary">5.1 Empresas Parceiras</h4>
                                <p>Compartilhamos dados necessários com empresas parceiras para processamento de bonificações e validação de transações.</p>

                                <h4 class="h5 text-secondary mt-4">5.2 Prestadores de Serviços</h4>
                                <p>Trabalhamos com terceiros que nos ajudam a operar a plataforma, incluindo:</p>
                                <ul>
                                    <li>Provedores de pagamento (PIX)</li>
                                    <li>Serviços de hospedagem e infraestrutura</li>
                                    <li>Ferramentas de análise e marketing</li>
                                    <li>Serviços de suporte ao cliente</li>
                                </ul>

                                <h4 class="h5 text-secondary mt-4">5.3 Obrigações Legais</h4>
                                <p>Podemos divulgar informações quando exigido por lei, ordem judicial ou autoridades competentes.</p>

                                <h4 class="h5 text-secondary mt-4">5.4 Proteção de Direitos</h4>
                                <p>Para proteger nossos direitos, propriedade, segurança ou a de nossos usuários.</p>
                            </section>

                            <section class="mb-5">
                                <h2 class="h3 text-primary mb-3">6. Segurança dos Dados</h2>
                                <p>Implementamos medidas técnicas e organizacionais apropriadas para proteger seus dados:</p>
                                <ul>
                                    <li>Criptografia de dados em trânsito e em repouso</li>
                                    <li>Controles de acesso rigorosos</li>
                                    <li>Monitoramento contínuo de segurança</li>
                                    <li>Treinamento regular da equipe sobre privacidade</li>
                                    <li>Auditorias de segurança regulares</li>
                                    <li>Planos de resposta a incidentes</li>
                                </ul>
                            </section>

                            <section class="mb-5">
                                <h2 class="h3 text-primary mb-3">7. Retenção de Dados</h2>
                                <p>Mantemos seus dados pessoais pelo tempo necessário para:</p>
                                <ul>
                                    <li>Fornecer nossos serviços</li>
                                    <li>Cumprir obrigações legais e regulatórias</li>
                                    <li>Resolver disputas</li>
                                    <li>Fazer cumprir nossos acordos</li>
                                </ul>
                                <p>Após esse período, os dados são excluídos de forma segura ou anonimizados.</p>
                            </section>

                            <section class="mb-5">
                                <h2 class="h3 text-primary mb-3">8. Seus Direitos</h2>
                                <p>De acordo com a LGPD, você tem os seguintes direitos:</p>
                                <ul>
                                    <li><strong>Confirmação:</strong> Saber se tratamos seus dados pessoais</li>
                                    <li><strong>Acesso:</strong> Obter cópia dos dados que mantemos sobre você</li>
                                    <li><strong>Correção:</strong> Solicitar correção de dados incompletos ou incorretos</li>
                                    <li><strong>Anonimização/Bloqueio:</strong> Solicitar anonimização ou bloqueio de dados</li>
                                    <li><strong>Eliminação:</strong> Solicitar exclusão de dados desnecessários</li>
                                    <li><strong>Portabilidade:</strong> Receber seus dados em formato estruturado</li>
                                    <li><strong>Revogação:</strong> Retirar consentimento a qualquer momento</li>
                                    <li><strong>Oposição:</strong> Opor-se ao tratamento baseado em legítimo interesse</li>
                                </ul>
                            </section>

                            <section class="mb-5">
                                <h2 class="h3 text-primary mb-3">9. Cookies e Tecnologias Similares</h2>
                                <p>Utilizamos cookies e tecnologias similares para:</p>
                                <ul>
                                    <li>Manter você logado na plataforma</li>
                                    <li>Lembrar suas preferências</li>
                                    <li>Analisar o uso da plataforma</li>
                                    <li>Personalizar conteúdo e anúncios</li>
                                    <li>Melhorar a segurança</li>
                                </ul>
                                <p>Você pode gerenciar suas preferências de cookies através das configurações do seu navegador.</p>
                            </section>

                            <section class="mb-5">
                                <h2 class="h3 text-primary mb-3">10. Transferência Internacional</h2>
                                <p>Alguns de nossos prestadores de serviços podem estar localizados fora do Brasil. Quando isso ocorrer, garantimos que:</p>
                                <ul>
                                    <li>A transferência seja feita para países com nível adequado de proteção</li>
                                    <li>Sejam implementadas salvaguardas apropriadas</li>
                                    <li>Seja obtido consentimento específico quando necessário</li>
                                </ul>
                            </section>

                            <section class="mb-5">
                                <h2 class="h3 text-primary mb-3">11. Menores de Idade</h2>
                                <p>Nossa plataforma não é direcionada a menores de 18 anos. Não coletamos intencionalmente dados pessoais de menores. Se tomarmos conhecimento de que coletamos dados de um menor, tomaremos medidas para excluí-los prontamente.</p>
                            </section>

                            <section class="mb-5">
                                <h2 class="h3 text-primary mb-3">12. Alterações nesta Política</h2>
                                <p>Podemos atualizar esta Política de Privacidade periodicamente. Quando isso ocorrer:</p>
                                <ul>
                                    <li>Publicaremos a versão atualizada em nossa plataforma</li>
                                    <li>Indicaremos a data da última atualização</li>
                                    <li>Notificaremos sobre mudanças significativas</li>
                                    <li>Solicitaremos novo consentimento quando necessário</li>
                                </ul>
                            </section>

                            <section class="mb-5">
                                <h2 class="h3 text-primary mb-3">13. Contato e Encarregado de Dados</h2>
